    def detect_stage(self, message: str) -> str:
        """
        检测面试阶段：preparation（面试前）/ during（面试中）/ after（面试后）
        """
        for stage, pattern in self._STAGE_PATTERNS.items():
            if pattern.search(message):
                return stage
        return "preparation"

    def detect_major(self, text_history: str, memories: Optional[List[dict]] = None) -> str:
        """
        根据对话历史 + 记忆列表推断用户的专业方向
        优先从记忆中找，其次从对话文本中找
        """
        # 优先检查记忆列表中的专业信息
        if memories:
            memory_text = " ".join([
                m.get("content", "") for m in memories
                if m.get("category") in ("personal", "career", "goal")
            ]).lower()
            for major, data in self.KNOWLEDGE_BASE.items():
                for keyword in data["keywords"]:
                    if keyword in memory_text:
                        logger.info(f"从记忆中识别到专业方向：{major}")
                        return major

        # 从对话历史中匹配
        text = text_history.lower()
        for major, data in self.KNOWLEDGE_BASE.items():
            for keyword in data["keywords"]:
                if keyword in text:
                    return major
        return "通用/文科"

    def get_interview_knowledge(self, major: str, stage: str = "preparation") -> str:
        """
        获取特定专业 + 面试阶段的完整知识片段，注入到 Prompt 中
        """
        kb = self.KNOWLEDGE_BASE.get(major)
        if not kb:
            # 即使是未知专业，也返回通用知识
            kb = self.KNOWLEDGE_BASE.get("通用/文科", {})

        focus_str = "、".join(kb.get("focus", []))
        q_list = kb.get("questions", [])
        q_str = "\n".join([f"  {i+1}. {q}" for i, q in enumerate(q_list[:5])])
        advice = kb.get("advice", "")
        stage_guide = self._STAGE_GUIDANCE.get(stage, self._STAGE_GUIDANCE["preparation"])

        return (
            f"【专业面试外挂知识库：{major}】\n"
            f"- 核心考查点：{focus_str}\n"
            f"- 高频面试题预警（TOP 5）：\n{q_str}\n"
            f"- 备考建议：{advice}\n\n"
            f"{stage_guide}\n"
            f"请结合以上行业特点和面试阶段，给用户制定针对性的准备建议或引导用户进行模拟面试。"
        )

    def get_common_questions(self, major: str) -> List[str]:
        """返回指定专业的 TOP 面试题列表"""
        kb = self.KNOWLEDGE_BASE.get(major)
        if kb:
            return kb.get("questions", [])
        # 未知专业返回通用题
        return self.KNOWLEDGE_BASE.get("通用/文科", {}).get("questions", [])

    def get_interview_tips_array(self) -> List[str]:
        """返回给前端 SSE 引用的 Tips 列表"""
        return self.COMMON_TIPS
